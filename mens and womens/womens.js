  let container= document.getElementById("womens");
  let cartLS = JSON.parse(localStorage.getItem("cart-page")) || [];
  function display(womensData){
      womensData.forEach(function (e){
          let Product=document.createElement("div");
          let img=document.createElement("img");
          img.src=e.image_url;

          let name=document.createElement("h5");
          name.innerText=e.name;

          let price=document.createElement("h5");
          price.innerText=e.price

          let button=document.createElement("button");
          button.innerText="Add to Cart"

          button.addEventListener("click",function(){
            if(addToCartFunc(e.name)===true){
              alert("item added succesfully");
              cartLS.push(e);
              localStorage.setItem("cart-page",JSON.stringify(cartLS))
            }else{
              alert("already added ");
            }
          })

          Product.append(img,name,price,button);
          container.append(Product);
      });
  }
  display(womensData);

  function addToCartFunc(name) {
    for (let i = 0; i < cartLS.length; i++) {
      if (cartLS[i].name == name) {
        return false;
      }
    }
    return true;
  }